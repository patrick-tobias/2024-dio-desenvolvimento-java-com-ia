package edu.dio.deploy.model.adapter;

import java.util.Collection;
import java.util.List;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import edu.dio.deploy.model.entity.Usuario.Usuario;

public class UserDetailsParaUsuarioAdapter implements UserDetails {
	
	private static final long serialVersionUID = 1L;
	private final Usuario usuario;
	
	public UserDetailsParaUsuarioAdapter(Usuario usuario) {
		this.usuario = usuario;
	}

	@Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return List.of(new SimpleGrantedAuthority("ROLE_USER"));
    }

    @Override
    public String getPassword() {
        return usuario.getSenha();
    }

    @Override
    public String getUsername() {
        return usuario.getEmail();
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
    
    public Usuario getUsuario() {
    	return this.usuario;
    }

}
